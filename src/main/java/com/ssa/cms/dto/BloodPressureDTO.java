package com.ssa.cms.dto;

/**
 *
 * @author Javed
 */
public class BloodPressureDTO {
    private Integer bloodPresureSeqNo;
    private Integer systolicBloodPressure;
    private Integer distolicBloodPressure;
    private Integer pulse;
    private String  readingTime;
    private Integer patientProfileSeqNo;
    private String rxNumber;
    private ComplianceRewardPointDTO rewardDTO;

    public Integer getBloodPresureSeqNo() {
        return bloodPresureSeqNo;
    }

    public void setBloodPresureSeqNo(Integer bloodPresureSeqNo) {
        this.bloodPresureSeqNo = bloodPresureSeqNo;
    }

    public Integer getSystolicBloodPressure() {
        return systolicBloodPressure;
    }

    public void setSystolicBloodPressure(Integer systolicBloodPressure) {
        this.systolicBloodPressure = systolicBloodPressure;
    }

    public Integer getDistolicBloodPressure() {
        return distolicBloodPressure;
    }

    public void setDistolicBloodPressure(Integer distolicBloodPressure) {
        this.distolicBloodPressure = distolicBloodPressure;
    }

    public Integer getPulse() {
        return pulse;
    }

    public void setPulse(Integer pulse) {
        this.pulse = pulse;
    }

    public String getReadingTime() {
        return readingTime;
    }

    public void setReadingTime(String readingTime) {
        this.readingTime = readingTime;
    }
    public Integer getPatientProfileSeqNo() {
        return patientProfileSeqNo;
    }

    public void setPatientProfileSeqNo(Integer patientProfileSeqNo) {
        this.patientProfileSeqNo = patientProfileSeqNo;
    }

    public String getRxNumber() {
        return rxNumber;
    }

    public void setRxNumber(String rxNumber) {
        this.rxNumber = rxNumber;
    }

    public ComplianceRewardPointDTO getRewardDTO() {
        return rewardDTO;
    }

    public void setRewardDTO(ComplianceRewardPointDTO rewardDTO) {
        this.rewardDTO = rewardDTO;
    }
    
}
